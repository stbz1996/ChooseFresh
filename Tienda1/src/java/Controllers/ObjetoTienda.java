package Controllers;


public class ObjetoTienda {
    public int id;
    public String nombre;
    public int precio;
    public String unidad;
    
    public ObjetoTienda(int id, String nombre, int precio, String unidad){
        this.id = id;
        this.nombre = nombre;
        this.precio = precio;
        this.unidad = unidad;
    }    
}
